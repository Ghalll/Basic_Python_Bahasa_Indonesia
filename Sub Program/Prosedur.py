# PROSEDUR 
"""
Adalah fungsi yang tidak menghasilkan nilai. 
Prosedur tidak mengharuskan adanya parameter input atau output
Prosedur didefinisikan dengan return tanpa ekspresi atau nilai yang dihasilkan di akhir fungsi.
"""

# Syntax dasar prosedur pada Python.
def sapa(nama):
    print(f"Halo, {nama}!")
# Memanggil prosedur
sapa("Ayu") 

# Contoh lain
def greeting(name):
    print("Halo " + name + ", Selamat Datang")
greeting("Ghaly")