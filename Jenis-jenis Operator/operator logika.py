# OPERATOR LOGIKA
"""
Operator logika merupakan jenis operator untuk melakukan operasi logika dengan kedua operannya bertipe boolean.
Hasil akhir dari operasi ini adalah nilai bertipe boolean.
"""
# ⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔
"""
▶️ "AND" atau "&" : Logika yang hanya menghasilkan True jika kedua operan bernilai True.
▶️ "OR" atau "|"  : Logika yang menghasilkan True jika salah satu dari kedua operan bernilai True. 
▶️  NOT    : Logika yang bertujuan untuk membalikkan nilai logika dari operannya.
"""

# Operator `and`
a = True
b = False
result = a and b
print(result)  # Output: False

# ⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔

# Operator `or`
a = True
b = False
result = a or b
print(result)  # Output: True

# ⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔

# Operator `not`
a = True
result = not a
print(result)  # Output: False
