# PERULANGAN WHILE LOOP
# While termasuk sintaks dalam Python yang bersifat indefinite iteration.
# Indefinite iteration adalah sebuah proses iterasi yang akan berhenti ketika memenuhi kondisi tertentu.
# Perulangan while digunakan untuk menjalankan suatu blok kode selama kondisi yang diberikan adalah True.
# ⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔⛔

num = 1
while num <= 10:
    print(num)
    num += 1

value = 10
while value > 0:
    print(value)
    value -= 1
