"""
Fungsi min() dan max() adalah fungsi bawaan (built-in) pada Python yang digunakan untuk
mengambil nilai minimum (terkecil) dan nilai maksimum (terbesar) dari suatu iterable (seperti list, 
tuple, atau string) atau urutan objek.
"""
angka = [13, 7, 24, 5, 96, 84, 71, 11, 38]
print(min(angka))
print(max(angka))

