# Fungsi count() digunakan untuk mengetahui berapa kali suatu nilai atau objek muncul dalam list.
# Umunya di gunakan pada sata string dan list.

# contoh pada string
text = "Hello, World!"
print(text.count("l"))  # Output: 3

# contoh pada list
genap = [2, 4, 4, 6, 6, 6, 8, 10, 10]
print(genap.count(6))   # Output: 3
