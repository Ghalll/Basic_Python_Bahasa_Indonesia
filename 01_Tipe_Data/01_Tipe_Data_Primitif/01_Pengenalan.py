"""
1). Integers (`int`) : Bilangan bulat positif atau negatif dan tidak memiliki angka desimal.
    contoh : `1`, `-20`, `999`, dan `0`.
2). Float (`float`) : Bilangan riil yang dapat mewakili bilangan bulat atau bilangan desimal.
    contoh : `3.14`, `-0.5`, `4.01E+1`.
3). Complex (`complex`) : Digunakan untuk merepresentasikan bilangan kompleks.
    contoh : `1+2j`.
4). Boolean (`bool`) :  tipe data yang hanya bernilai TRUE atau FALSE.
    contoh : `True`, `False`.
5). String (`str`) : Digunakan untuk merepresentasikan teks atau karakter.
    contoh : `"Hello, World!"`, `'Pyhton'`.
6). None (`NoneType`) : Digunakan untuk menyatakan ketiadaan nilai atau variabel yang tidak memiliki nilai.
    contoh : `None`
"""
