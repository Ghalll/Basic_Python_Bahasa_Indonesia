# Class 
"""
Pembuatan Class mirip seperti fungsi, yakni perlu menggunakan keyword. keywordnya yaitu `class`.
"""

# Membuat class
class Car:
  # Atribut
  warna = "Merah"

# Membuat objek
# Objek yang merupakan perwujudan dari kelas yang telah dibuat.
car1 = Car()
print(car1.warna)
car1.warna = "Hitam"
print(car1.warna)